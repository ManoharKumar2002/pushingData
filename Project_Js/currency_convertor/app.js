for (const select of dropdowns){
  for(currCode in countryList){
        let newOption = document.createElement("option");
        newOption.value = currCode ;
        newOption.innerText = currCode ;
        if(select.name === "from" && currCode === "USD"){
          newOption.selected = "selected" ;
        }else if(select.name === "to" && currCode === "INR"){
          newOption.selected = "selected" ;
        }
        select.append(newOption);
      };
      select.addEventListener("change",(e) => {
          updateOpt(e.target);
      })
};

const updateOpt = (elem)=>{
    let currCode = elem.value ;
    let countryCode = countryList[currCode] ;
    let newSrc = `https://flagsapi.com/${countryCode}/flat/64.png` ;
    let img = elem.parentElement.querySelector("img");
    img.src = newSrc ;
}


btn.addEventListener("click", async (evt)=>{
    evt.preventDefault();
    let amount = document.querySelector(".amount input");
    let amtValue = amount.value ;
    if(amtValue == "" || amtValue < 1){
      amtValue = 1 ;
      amount.value = "1"
    }
    // console.log(fromURL.value.toLowerCase(),toURL.value.toLowerCase());
    const URL = `${BASE_URL}${fromCurr.value}`//.json`;
    let response = await fetch(URL);
    let data = await response.json() ;
    let rates = data.conversion_rates[toCurr.value];
    let finalAmt = amtValue * rates ;
    msg.innerText = `${amtValue} ${fromCurr.value} = ${finalAmt} ${toCurr.value} `
});




